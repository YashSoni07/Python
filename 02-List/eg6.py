# To clear() # Remove all items from list.
l1 = [10, 20, 30, 40, 50]
l1.clear()
print(l1.clear())

# count() # Return number of occurrences of value.
l2 = [10, 20, 30, 40, 50, 10, 20, 30, 40, 50]
print(l2.count(50))

# copy() # Return a shallow copy of the list.
l3 = [10, 20, 30, 40, 5010, 20, 30, 40, 50]
print(l3)
print(l3.copy())

# remove() # Remove first occurrence of value.
l4 = [10, 20, 30, 40, 5010, 20, 30, 40, 50]
l4.remove(5010)
print(l4)
