from numpy import *

a1 = array([10, 20, 30, 40, 50])
a2 = array([10, 20, 30, 40, 50])

print(a1 + a2)  # [ 20  40  60  80 100]

print(a1 - a2)  # [0 0 0 0 0]

print(sin(a1))  # [-0.54402111  0.91294525 -0.98803162  0.74511316 -0.26237485]

print(cos(a1))  # [-0.83907153  0.40808206  0.15425145 -0.66693806  0.96496603]

print(min(a1))  # 10

print(max(a1))  # 50

print(sum(a1))  # 150
