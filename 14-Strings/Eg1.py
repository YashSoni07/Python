# String Concatenation
fistName = 'Yash'
lastName = 'Soni'

print(fistName + lastName)
print(fistName, lastName)

# Repetition

print(fistName * 2)

# Escape Sequences

print('hello \n python')
print('hello \t python')
print('hello \' python')
print('hello \" python')
print('hello \\ python')
print('hello \b python')

# Combining Repetition and Concentration

print('A' + 'B' * 2)
print('A' * 2 + 'B')

a1 = 'HelloPython'
print(a1)

print(a1.upper())
print(a1.lower())
print(a1.swapcase())
print(a1.title())


