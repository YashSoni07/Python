# Split Method

a2 = 'Hello World Hello Python'
print(a2.split())

a3 = 'Hello World Hello Python'
print(a3.split(" "))

a4 = 'Hello-World-Hello-Python'
print(a4.split("-"))

a5 = 'Hello:World:Hello:Python'
print(a5.split(":"))

a6 = 'Hello Java And Hello Python And Hello HTML'
print(a6.split('and'))
print(a6.split('and', 1))
